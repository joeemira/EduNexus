    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String name = jTextField1.getText().trim();
String email = jTextField2.getText().trim();
String number = jTextField3.getText().trim();
String day = jTextField4.getText().trim();
String month = jTextField5.getText().trim();
String year = jTextField6.getText().trim();

try (Connection conn = DBConnection.connect()) {
    String birthDateString = year + "-" + month + "-" + day;
    Date birthDate = Date.valueOf(birthDateString);

    String sql = 
        "INSERT INTO student_profiles (user_id, full_name, email, phone_number, birth_date)"
        +"VALUES (?, ?, ?, ?, ?)"
        +"ON CONFLICT (user_id) DO UPDATE "
        +"SET full_name = EXCLUDED.full_name," 
        +"email = EXCLUDED.email, "
        +"phone_number = EXCLUDED.phone_number,"
        +"birth_date = EXCLUDED.birth_date";

    PreparedStatement stmt = conn.prepareStatement(sql);
    stmt.setInt(1, Tocken.id); // current student ID
    stmt.setString(2, name);
    stmt.setString(3, email);
    stmt.setString(4, number);
    stmt.setDate(5, birthDate);
    stmt.executeUpdate();

    JOptionPane.showMessageDialog(this, "Profile saved successfully!");

} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error saving profile: " + e.getMessage());
    e.printStackTrace();
}

    }//GEN-LAST:event_jButton1ActionPerformed

