    public StudentRegister() {
        initComponents();
        try (Connection conn = DBConnection.connect()) {
    String sql = "SELECT full_name, email, phone_number, birth_date FROM student_profiles WHERE user_id = ?";
    PreparedStatement stmt = conn.prepareStatement(sql);
    stmt.setInt(1, Tocken.id); // Assuming Tocken holds the logged-in user's ID
    ResultSet rs = stmt.executeQuery();

    if (rs.next()) {
        jTextField1.setText(rs.getString("full_name"));
        jTextField2.setText(rs.getString("email"));
        jTextField3.setText(rs.getString("phone_number"));

        java.sql.Date birthDate = rs.getDate("birth_date");
        if (birthDate != null) {
            java.util.Calendar cal = java.util.Calendar.getInstance();
            cal.setTime(birthDate);
            jTextField4.setText(String.valueOf(cal.get(java.util.Calendar.DAY_OF_MONTH)));
            jTextField5.setText(String.valueOf(cal.get(java.util.Calendar.MONTH) + 1));
            jTextField6.setText(String.valueOf(cal.get(java.util.Calendar.YEAR)));
        }
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Failed to load profile: " + e.getMessage());
    e.printStackTrace();
}

    }

